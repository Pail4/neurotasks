% Создаем вектор x от -3 до 3 с шагом 0.1
x = -3:0.1:3;

% Вычисляем значения функции y = nthroot((x*x - 1), 3)
y = nthroot((x.^2 - 1), 3);

% Строим график
plot(x, y, '-g*'); % сплошная зеленая линия с точками '*'
title('График функции y = nthroot((x^2 - 1), 3)');
xlabel('x');
ylabel('y');
grid on; % добавляем сетку
